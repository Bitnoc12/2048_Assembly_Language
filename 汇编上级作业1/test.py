def main():
    input_str1 = input("Enter number1: ")
    input_str2 = input("Enter number2: ")

    num1 = int(input_str1)
    num2 = int(input_str2)

    result_num = num1 * num2

    print(f"The result of multiplying {input_str1} and {input_str2} is: {result_num}")

if __name__ == "__main__":
    main()
